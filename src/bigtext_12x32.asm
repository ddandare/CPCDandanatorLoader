;DrawDigit - Draw a Digit (2 digits) in screen
;	IN	- A=Digit to paint (value 0-32)
;		- HL=ADDR in screen
DrawDigit:
				CP		33
				JR	C,DrawDigitNumber			;If less than 33 then draw numbers
			;Draw Er = Error
				LD		DE,BNE
				PUSH	HL
				CALL	DrawNumber
				POP		HL
				LD		B,6
DrawDigit_Err:				
				INC		 HL
				DJNZ DrawDigit_Err
				LD		DE,BNr
				CALL	DrawNumber
				RET
				
DrawDigitNumber:
				LD		D,0
DrawDigit_Loop:
				CP		10
				JR		C,DrawDigit_Print
				INC		D
				SUB		10
				JR		DrawDigit_Loop
DrawDigit_Print:
				PUSH	HL
				LD		HL,BN0
				LD		C,A					;C=Units
				LD		A,D					;A=Tens
				AND		A
				JR		Z,DrawDigit_PrintTens
				LD		DE,BN1-BN0
DrawDigit_CalcTens:
				ADD		HL,DE				;Next Digit
				DEC		A
				JR		NZ,DrawDigit_CalcTens
DrawDigit_PrintTens:
				EX		DE,HL
				POP		HL
				PUSH	HL
				PUSH	BC
				CALL	DrawNumber
				POP		BC
				
				LD		HL,BN0
				LD		A,C					;A=Units
				AND		A
				JR		Z,DrawDigit_PrintUnits
				LD		DE,BN1-BN0
DrawDigit_CalcUnits:
				ADD		HL,DE				;Next Digit
				DEC		A
				JR		NZ,DrawDigit_CalcUnits
DrawDigit_PrintUnits:
				EX		DE,HL
				POP		HL
				LD		B,6
DrawDigit_PrintNext:				
				INC		 HL
				DJNZ DrawDigit_PrintNext
				CALL	DrawNumber
				
				RET
		
;DrawNumber - Draw a number in screen
;	IN	- HL=ADDR in screen
;		- DE=ADDR of number
DrawNumber:
				LD		B,4				;	Total of Blocks of 8 scanlines
DrawNumberChar:
				PUSH    BC
				
				PUSH	HL
				
				LD		B,8				;  1st 8 scanlines
DrawNumber_LoopExt:				
				LD		C,6				;	6 bytes to send (12 pixels)
				PUSH	HL
DrawNumber_Loop:				
				LD		A,(DE)
				LD		(HL),A
				INC		DE
				INC		HL
				DEC		C
				JR		NZ,DrawNumber_Loop

				POP		HL
				LD		A,H
				ADD		A,8
				LD		H,A
				DJNZ DrawNumber_LoopExt
				POP		HL
				LD		BC,80
				ADD		HL,BC
				
				POP    BC
				
				DJNZ DrawNumberChar
				
				RET
				
				
