;DrawBar - Draw Bar in Screen
;	IN	- HL=ADDR in screen
DrawBar:
				LD		DE,Bar
				LD		B,8				;  8 scanlines
DrawBar_Loop:				
				LD		A,(DE)
				LD		(HL),A
				INC		DE
				INC		HL
				LD		A,(DE)
				LD		(HL),A
				INC		DE
				DEC		HL
				LD		A,H
				ADD		A,8
				LD		H,A
				DJNZ DrawBar_Loop

				RET
		
